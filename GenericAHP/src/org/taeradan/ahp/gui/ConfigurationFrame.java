		private void jTreeAhpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTreeAhpMouseClicked
			DefaultMutableTreeNode node = (DefaultMutableTreeNode)jTreeAhp.getLastSelectedPathComponent();
			Object object = node.getUserObject();
			System.out.print(evt.getButton());
			String message = null;
			if(object instanceof Root){
				Root root = (Root)object;
				message=root.treeToString();
			}
			if(object instanceof Criteria){
				Criteria criteria = (Criteria)object;
				message=criteria.treeToString();
				CriteriaDialog dialogue = new CriteriaDialog(this, true, criteria);
				dialogue.setVisible(true);
			}
			if(object instanceof Indicator){
				Indicator indicator = (Indicator)object;
				message=indicator.toString();
				IndicatorDialog dialogue = new IndicatorDialog(this, true, indicator);
				dialogue.setVisible(true);
			}

		}//GEN-LAST:event_jTreeAhpMouseClicked
	
